export const SPEEDS: Speed[] = TABLE.map(([label, seconds], index) => ({
	index,
	label,
	seconds,
	ev: Math.log2(1 / seconds)
}));

const labelMap = new Map<string, number>(
	SPEEDS.map((s) => [s.label, s.index])
);

export function labelToIndex(label: string): number {
	return labelMap.get(label) ?? -1;
}

export function indexToSpeed(index: number): Speed {
	return SPEEDS[index];
}
